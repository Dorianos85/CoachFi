export type Locale = "en" | "pl" | "ja";

type DeepPartial<T> = {
  [K in keyof T]?: T[K] extends Array<infer U>
    ? Array<U>
    : T[K] extends object
      ? DeepPartial<T[K]>
      : T[K];
};

function mergeTranslations(base: Translations, overrides: DeepPartial<Translations>): Translations {
  const output: Record<string, unknown> = { ...base };

  for (const [key, value] of Object.entries(overrides)) {
    const baseValue = output[key];
    if (
      value &&
      typeof value === "object" &&
      !Array.isArray(value) &&
      baseValue &&
      typeof baseValue === "object" &&
      !Array.isArray(baseValue)
    ) {
      output[key] = mergeTranslations(baseValue as Translations, value as DeepPartial<Translations>);
    } else {
      output[key] = value;
    }
  }

  return output as Translations;
}

